public class DistributionLaw {
    private String kindDistributionLaw;
    private Double expectedValue;
    private Double dispersion;
    private Double left;
    private Double right;
    private Double intensity;

    public String getKindDistributionLaw() {
        return kindDistributionLaw;
    }

    public void setKindDistributionLaw(String kindDistributionLaw) {
        this.kindDistributionLaw = kindDistributionLaw;
    }

    public Double getExpectedValue() {
        return expectedValue;
    }

    public void setExpectedValue(Double expectedValue) {
        this.expectedValue = expectedValue;
    }

    public Double getDispersion() {
        return dispersion;
    }

    public void setDispersion(Double dispersion) {
        this.dispersion = dispersion;
    }

    public Double getLeft() {
        return left;
    }

    public void setLeft(Double left) {
        this.left = left;
    }

    public Double getRight() {
        return right;
    }

    public void setRight(Double right) {
        this.right = right;
    }

    public Double getIntensity() {
        return intensity;
    }

    public void setIntensity(Double intensity) {
        this.intensity = intensity;
    }
}
